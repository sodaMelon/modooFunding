package vo;

public class FunderView {
	
	private int pnum;
	private String id;
	private int funded;
	private int status;
	private int payment;
	
	public int getPnum() {
		return pnum;
	}
	public void setPnum(int pnum) {
		this.pnum = pnum;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getFunded() {
		return funded;
	}
	public void setFunded(int funded) {
		this.funded = funded;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public int getPayment() {
		return payment;
	}
	public void setPayment(int payment) {
		this.payment = payment;
	}
	
	
	
}
